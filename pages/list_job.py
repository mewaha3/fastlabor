    df_find = load_sheet("find_job")

    # ‚úÖ Tabs: Post Job / Find Job
    tab1, tab2 = st.tabs(["üìå Post Job", "üîç Find Job"])

    with tab1:
        st.subheader("üìå ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£‡πÇ‡∏û‡∏™‡∏ï‡πå‡∏á‡∏≤‡∏ô")
        if not df_post.empty:
            st.dataframe(df_post, use_container_width=True)
